public class Cat {
    private String Name ;
    private String color;
    private int age;
    private int weight;
    private int length;

    public Cat(String name, String color, int age, int weight, int length) {
        Name = name;
        this.color = color;
        this.age = age;
        this.weight = weight;
        this.length = length;
    }
}
